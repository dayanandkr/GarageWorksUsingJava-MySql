    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
         try{
            int ri=jTable1.getSelectedRow();
            if(ri!=-1){
                int id=(int)jTable1.getValueAt(ri,0);
                String name=(String)jTable1.getValueAt(ri,1);
                String phone=(String)jTable1.getValueAt(ri,2);
                 String v_no=(String)jTable1.getValueAt(ri,3);
                String v_name=(String)jTable1.getValueAt(ri,4);
                int amt=Integer.parseInt(t2.getText());
                
                String pMode;
                if(radio_c.isSelected())
                    pMode=radio_c.getText();
                else if(radio_d.isSelected())
                    pMode=radio_d.getText();
                else if(radio_cr.isSelected())
                    pMode=radio_cr.getText();
                else 
                    pMode=radio_on.getText();
                
                Db_Connection.ConectionDb.st.executeUpdate("insert into bills (client_id,name,phone,amount,bill_date,v_nm,v_no,payment_mode) values("+id+",'"+name+"','"+phone+"',"+amt+",CURRENT_DATE,'"+v_name+"','"+v_no+"','"+pMode+"')");
                Db_Connection.ConectionDb.st.executeUpdate("update clients set status='done' where id="+id);
                getEntries();
                getEntriesBillItems();
                t2.setText("0");
                t4.setText("250");
                JOptionPane.showMessageDialog(null, "Bill saved Successfully!");
            }else{
                JOptionPane.showMessageDialog(null, "Please Select any row!");
            }
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null, ex);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

