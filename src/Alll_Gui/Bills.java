    void getSparePartNames(){
        try{
            cb.removeAllItems();
            ResultSet rs=Db_Connection.ConectionDb.st.executeQuery("select * from spare_parts where qty>0");
            while(rs.next()){
                String name=rs.getString("name");
                int price=rs.getInt("price");
                spareParts.put(name, price);
                cb.addItem(name);
            }
            rs.close();
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null, ex);
        }
    }
    
    void getEntries(){
        try{
            javax.swing.table.DefaultTableModel dtm=(javax.swing.table.DefaultTableModel)jTable1.getModel();
            //remove row from JTable
            int rc=dtm.getRowCount();
            for (int i = 1; i <= rc; i++) {
                dtm.removeRow(0);
            }
            //add row to JTable
            ResultSet rs=Db_Connection.ConectionDb.st.executeQuery("select * from clients where status='working'");
            while(rs.next()){
                java.util.Vector r=new java.util.Vector();
                r.add(rs.getInt("id"));
                r.add(rs.getString("name"));
                r.add(rs.getString("phone"));
                r.add(rs.getString("v_no"));
                r.add(rs.getString("v_name"));
                r.add(rs.getString("jobs"));
                r.add(rs.getString("entry_date"));
                dtm.addRow(r);
            }
            rs.close();
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null, ex);
        }
    }
    
    
    void getEntriesBillItems(){
        try{
            int ri=jTable1.getSelectedRow();
            if(ri!=-1){
                int id=(int)jTable1.getValueAt(ri,0);
            
                javax.swing.table.DefaultTableModel dtm=(javax.swing.table.DefaultTableModel)jTable2.getModel();
                //remove row from JTable
                int rc=dtm.getRowCount();
                for (int i = 1; i <= rc; i++) {
                    dtm.removeRow(0);
                }
                //add row to JTable
                int total=0;
                ResultSet rs=Db_Connection.ConectionDb.st.executeQuery("select * from bill_items where client_id="+id);
                while(rs.next()){
                    java.util.Vector r=new java.util.Vector();
                    r.add(rs.getInt("bill_id"));
                    r.add(rs.getString("s_name"));
                    int q=rs.getInt("s_qty");
                    r.add(q);
                    int rt=rs.getInt("s_rate");
                    r.add(rt);
                    int p=rs.getInt("s_price");
                    total += p;
                    r.add(p);
                    dtm.addRow(r);
                }
                rs.close();
                t2.setText(total+"");
                int service=Integer.parseInt(sc.getText());
                int grand=total+service;
                t4.setText(grand+"");
            }else{
                javax.swing.table.DefaultTableModel dtm=(javax.swing.table.DefaultTableModel)jTable2.getModel();
                //remove row from JTable
                int rc=dtm.getRowCount();
                for (int i = 1; i <= rc; i++) {
                    dtm.removeRow(0);
                }
            }
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null, ex);
        }
    }
    
